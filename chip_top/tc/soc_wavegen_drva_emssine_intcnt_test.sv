    	  `nnc_info("SOC_TEST", "Read interrupt register", NNC_LOW)
    	  `RD_BURST_WAVEGEN_REG(top_test_cfg.reg_addr, top_test_cfg.no_of_bytes, top_test_cfg.rd_data);	
	  if ((top_test_cfg.rd_data[2][3]===1'b1) & (top_test_cfg.rd_data[2][2:0]===`DUT_IF.wg_drv_sel)) begin //also check if interrupt in this block is enabled (bit 8 is active)
		if((top_test_cfg.rd_data[2][5]===1) & (top_test_cfg.rd_data[2][4]===0)) begin
		    `nnc_info("SOC_TEST", $sformatf("Interrupt is in block no: %d", `DUT_IF.wg_drv_sel), NNC_LOW)
		    `nnc_error("SOC_TEST", $sformatf("Interrupt WARNING: Second address (underflow) interrupt has happenned at address: %h", top_test_cfg.rd_data[0]))
		    // ------------------------------------------------------------------
    		    // Write to ADDR_WG_DRV_INT_REG01 (clear the second address interrupt)
    		    // ------------------------------------------------------------------
    		    assert(top_test_cfg.randomize() with {reg_addr == (`SOC_ADDR_WG_DRV_INT_REG01 + (`WAVEGEN_1_ADDR_BASE * `DUT_IF.wg_drv_sel)); wr_data[0] == 8'h05;});
    		    `nnc_info("SOC_TEST", "Clear the second address interrupt", NNC_LOW)
    		    `WR_WAVEGEN_REG(top_test_cfg.reg_addr, top_test_cfg.wr_data[0], top_test_cfg.pads);//hopefully this interrupt is never activated otherwise we have missed the chance to load the next half of data on time
		end
		if(top_test_cfg.rd_data[2][4]===1) begin
		    `nnc_info("SOC_TEST", $sformatf("Interrupt is in block no: %d", `DUT_IF.wg_drv_sel), NNC_LOW)
		    `nnc_info("SOC_TEST", $sformatf("First address interrupt has happenned at address: %h", top_test_cfg.rd_data[1]), NNC_LOW)

		    if (top_test_cfg.rd_data[1] === 8'h00) begin //first address is at the first half of the wave
			// --------------------------------------------------------
    			// Write to ADDR_WG_DRV_INT_REG01 (clear the interrupt)
    			// --------------------------------------------------------
			fork
			begin
    			assert(top_test_cfg.randomize() with {reg_addr == (`SOC_ADDR_WG_DRV_INT_REG01 + (`WAVEGEN_1_ADDR_BASE * `DUT_IF.wg_drv_sel)); wr_data[0] == 8'h03;});
    			`nnc_info("SOC_TEST", "Clear the first address interrupt", NNC_LOW)
    			`WR_WAVEGEN_REG(top_test_cfg.reg_addr, top_test_cfg.wr_data[0], top_test_cfg.pads);
			end
			begin
			//Make sure interrupt is cleared
			wait(`SOC_TB.INTB === 0);
			end
			join
			for (int j=0; j<64; j++) begin
			    // --------------------------------------------------------
    			    // Write to ADDR_WG_DRV_IN_WAVE_ADDR_REG0
    			    // --------------------------------------------------------
    			    assert(top_test_cfg.randomize() with {reg_addr == (`SOC_ADDR_WG_DRV_IN_WAVE_ADDR_REG0 + (`WAVEGEN_1_ADDR_BASE * `DUT_IF.wg_drv_sel)); wr_data[0] == j;});
    			    `WR_WAVEGEN_REG(top_test_cfg.reg_addr, top_test_cfg.wr_data[0], top_test_cfg.pads);
			    if (d===3) begin// if d==3, then we are in off state. make all registers zero
				`nnc_info("SOC_TEST", "Clear the wave data", NNC_LOW)
			    	// --------------------------------------------------------
    			    	// Write to ADDR_WG_DRV_IN_WAVE_REG01
    			    	// --------------------------------------------------------
			    	assert(top_test_cfg.randomize() with {reg_addr == (`SOC_ADDR_WG_DRV_IN_WAVE_REG01 + (`WAVEGEN_1_ADDR_BASE * `DUT_IF.wg_drv_sel)); wr_data[0] == 8'h00;});
    			    	`WR_WAVEGEN_REG(top_test_cfg.reg_addr, top_test_cfg.wr_data[0], top_test_cfg.pads);
			    end
			    else begin
				`nnc_info("SOC_TEST", $sformatf("Update the new wave data r: %d", r), NNC_LOW)
				// --------------------------------------------------------
    			    	// Write to ADDR_WG_DRV_IN_WAVE_REG01
    			    	// --------------------------------------------------------
				if((s===0) && (r<ramp_cnt)) begin
				    val = r+1;
				end
				else if((s===2) && (r>0)) begin
				    val = r-1;
				end
				else begin
				    val = r;
				end
				assert(top_test_cfg.randomize() with 
				{reg_addr == (`SOC_ADDR_WG_DRV_IN_WAVE_REG01 + (`WAVEGEN_1_ADDR_BASE * `DUT_IF.wg_drv_sel)); wr_data[0] == (top_test_cfg.sawtooth_data[j] * val);});
    			    	`WR_WAVEGEN_REG(top_test_cfg.reg_addr, top_test_cfg.wr_data[0], top_test_cfg.pads);
			    end
			end

			//now depending on the which part of the EMS wave, change wave shape:
			case (s)
				0: begin //ramp up
				 if (r<ramp_cnt) begin
				    r = r + 1; //next wave shape
				    `nnc_info("SOC_TEST", "Ramp up!!!", NNC_LOW)
				 end
				 else begin
				    s = 1; //go the hold state 1
				 end
				end
				1: begin//hold
				 if (c < hold_cnt) begin//stay in the hold for 'hold_cnt' periods of the saw tooth wave 
				    `nnc_info("SOC_TEST", "Hold!!!", NNC_LOW)
				    c = c + 1;
				 end
				 else begin
				    c = 0;
				    s = 2;
				 end
				end
				2: begin //ramp down
				 if (r>0) begin
				    r = r - 1;
				    `nnc_info("SOC_TEST", "Ramp down!!!", NNC_LOW)
				 end
				 else begin
				    if (d==0) begin//if driver 0 is active, turn it off and start driver 1
					// --------------------------------------------------------
    					// Write to ADDR_WG_DRV_CTRL_REG0
    					// --------------------------------------------------------
    					assert(top_test_cfg.randomize() with {reg_addr == `SOC_ADDR_WG_DRV_CTRL_REG0; wr_data[0] == 8'h06;});
    					`nnc_info("SOC_TEST", "Disable driver0 using control register", NNC_LOW)
    					`WR_WAVEGEN_REG(top_test_cfg.reg_addr, top_test_cfg.wr_data[0], top_test_cfg.pads);
					// ------------------------------------------------------------------
    		    			// Write to ADDR_WG_DRV_INT_REG01 (clear any pending interrupts)
    		    			// ------------------------------------------------------------------
    		    			assert(top_test_cfg.randomize() with {reg_addr == `SOC_ADDR_WG_DRV_INT_REG01; wr_data[0] == 8'h07;});
    		    			`nnc_info("SOC_TEST", "Clear any pending interrupts of driver0", NNC_LOW)
    		    			`WR_WAVEGEN_REG(top_test_cfg.reg_addr, top_test_cfg.wr_data[0], top_test_cfg.pads);
					// --------------------------------------------------------
    					// Write burst starting from ADDR_WG_DRV_INT_REG01
    					// --------------------------------------------------------
    					assert(top_test_cfg.randomize() with {reg_addr == (`SOC_ADDR_WG_DRV_INT_REG01 + `WAVEGEN_1_ADDR_BASE); no_of_bytes == 3; wr_data[0] == 8'h1f; wr_data[1] == 8'h00; wr_data[2] == 8'h01;});
    					`nnc_info("SOC_TEST", "Configure driver1 interrupt register", NNC_LOW)//interrupt register set as first address 0; second address 31
    					`WR_BURST_WAVEGEN_REG(top_test_cfg.reg_addr, top_test_cfg.no_of_bytes, top_test_cfg.pads, top_test_cfg.wr_data);
					#100us;//wait 1pclk before enabling driver after configuring int_reg
					// --------------------------------------------------------
    					// Write to ADDR_WG_DRV_CTRL_REG0
    					// --------------------------------------------------------
    					assert(top_test_cfg.randomize() with {reg_addr == (`SOC_ADDR_WG_DRV_CTRL_REG0 + `WAVEGEN_1_ADDR_BASE); wr_data[0] == 8'h07;});
    					`nnc_info("SOC_TEST", "Enable driver1 using control register", NNC_LOW)
    					`WR_WAVEGEN_REG(top_test_cfg.reg_addr, top_test_cfg.wr_data[0], top_test_cfg.pads);
					`DUT_IF.wg_drv_sel = 1;
					d = 1;
					s = 0; //start all over again but for driver 1
				    end
				    else begin
					d = 3; //driver 1 is also done now
					s = 3; //go to off state for both drivers. We don't disable driver 1 because we won't recieve interrupts. We can disable and use microcontroller delay instead
				    end
				 end
				end
				3: begin//off
				 if (c < off_cnt) begin//stay in the off state for 'off_cnt' periods of the saw tooth wave
				    `nnc_info("SOC_TEST", "OFF state!!!", NNC_LOW)
				    c = c + 1;
				 end
				 else begin//enable driver 0 again and disable driver 1
				    // --------------------------------------------------------
    				    // Write to ADDR_WG_DRV_CTRL_REG0
    				    // --------------------------------------------------------
    				    assert(top_test_cfg.randomize() with {reg_addr == `SOC_ADDR_WG_DRV_CTRL_REG0; wr_data[0] == 8'h07;});
    				    `nnc_info("SOC_TEST", "Enable driver0 using control register", NNC_LOW)
    				    `WR_WAVEGEN_REG(top_test_cfg.reg_addr, top_test_cfg.wr_data[0], top_test_cfg.pads);
    				    assert(top_test_cfg.randomize() with {reg_addr == (`SOC_ADDR_WG_DRV_CTRL_REG0 + `WAVEGEN_1_ADDR_BASE); wr_data[0] == 8'h06;});
    				    `nnc_info("SOC_TEST", "Disable driver1 using control register", NNC_LOW)
    				    `WR_WAVEGEN_REG(top_test_cfg.reg_addr, top_test_cfg.wr_data[0], top_test_cfg.pads);
				    // ------------------------------------------------------------------
    		    		    // Write to ADDR_WG_DRV_INT_REG01 (clear any pending interrupts)
    		    		    // ------------------------------------------------------------------
    		    		    assert(top_test_cfg.randomize() with {reg_addr == (`SOC_ADDR_WG_DRV_INT_REG01 + `WAVEGEN_1_ADDR_BASE); wr_data[0] == 8'h07;});
    		    		    `nnc_info("SOC_TEST", "Clear any pending interrupts of driver1", NNC_LOW)
    		    		    `WR_WAVEGEN_REG(top_test_cfg.reg_addr, top_test_cfg.wr_data[0], top_test_cfg.pads);
				    `DUT_IF.wg_drv_sel = 0;
				    c = 0;
				    s = 0; //start all over again
				    d = 0;
				 end
				end
			endcase
		    end
		end
	end
    end
  endtask

