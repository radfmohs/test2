  task wavegen_drv_int_update;
    int s = 0;
    int c = 0;
    int r = 0;
    int d = 0;
    int val = 0;

`ifdef BEHAVIORAL
    int ramp_cnt = 8;
    int hold_cnt = 5;
    int off_cnt = 4;
    repeat(50) begin
`else
    int ramp_cnt = 4;
    int hold_cnt = 1;
    int off_cnt = 2;
    repeat(20) begin
`endif
	@(posedge`SOC_TB.INTB);
	`nnc_info("SOC_TEST", "An interrupt happenned ...", NNC_LOW)
	//for (int i=0; i<1; i++) begin //only one of the Driver A is working in this test case
	  // ------------------------------------------------------------------------------------------------------------------------------
    	  // Read burst starting from ADDR_WG_DRV_INT_REG01 (/read the interrupt registers to find the block which generated the interrupt)
